// Name of file MUST match class name
// A class should always start with an uppercase letter
// Java is case-sensitive
public class Main {
    public static void main(String[] args) {
        // Strings must be wrapped in double quotes
        // The 'print' method also works, but doesn't leave a newline at the end
        System.out.println("Hello world!");
    }
}

// To compile: javac Main.java
// To run: java Main
